<script>
  let rating = 0;

  // Handle rating clicks
  function rateProject(value) {
    rating = value;
    document.getElementById('rating-value').value = rating;
    
    // Highlight selected stars
    let stars = document.querySelectorAll('.star');
    stars.forEach((star, index) => {
      if (index < rating) {
        star.style.color = 'gold';
      } else {
        star.style.color = 'gray';
      }
    });
  }

  // Handle feedback submission
  function submitFeedback() {
    let name = document.getElementById('name').value;
    let email = document.getElementById('email').value;
    let feedback = document.getElementById('feedback').value;
    let ratingValue = document.getElementById('rating-value').value;

    if (ratingValue == 0 || !name || !email || !feedback) {
      alert("Please fill in all fields and provide a rating.");
      return;
    }

    // Send feedback data using AJAX (POST request)
    let formData = new FormData();
    formData.append('name', name);
    formData.append('email', email);
    formData.append('rating', ratingValue);
    formData.append('feedback', feedback);

    fetch('submit_feedback.php', {
      method: 'POST',
      body: formData
    })
    .then(response => response.text())
    .then(data => {
      document.getElementById('feedback-response').innerText = data;
      document.getElementById('feedback-form').reset(); // Reset the form
      fetchFeedback(); // Refresh the feedback list
    })
    .catch(error => console.error('Error submitting feedback:', error));
  }

